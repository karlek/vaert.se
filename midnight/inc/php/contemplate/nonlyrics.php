         <h2>Why I prefer songs without lyrics</h2>
         <article>

            <figure>
               <img src="inc/img/ssh.png" alt="###">
            </figure>

            <p>When I listen to music I almost immediately change the way I think and perceive the world. Sometimes it feels like I let my subconsciousness take over my body and make my mind go blank. The feeling can be described as letting a close friend take responsibility over your body. Other times I get spikes of ideas and creativity somehow orchestrated from the rhythm and melodies of the music.</p>

            <p>However when I listen to songs where someone sings in a language I can understand, I feel limited to the specific feeling that the artist felt when composing the song. This feeling is non-present when I listen to instrumental music, where it instead feels like the artist created a whole playground just for me. Here I can experience, and philosophy about, my thoughts and feelings.</p>

            <p>It is the same when I listen to songs with vocals in languages I don't understand. The words can become a perfect medium which is breathtakingly beautiful, while still giving me a free leash on my imagination.</p>

            <p>As I grew older I've experienced many life changing events and while listening to songs I couldn't understand, I created my own imaginative meaning behind the lyrics. And I tried to understand my own meaning behind the songs by learning the languages which they were sung in. This discovery of my own search for self revelation is making me feel embarrassed and at the same time happy that I'm so curious.</p>

